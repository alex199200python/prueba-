
# crear un programa que me sume dos valores y me diga siu es par o inpar

num1 = int(input("ingrese el primer numero:"))
num2 = int(input("ingrese el segundo numero:"))

suma = num1 + num2

 
print("la suma es:" ,suma)

if suma % 2 == 0:
    print("el resultado es un numero par")
else:
    print("el resultado es un numero inpar")  


 

